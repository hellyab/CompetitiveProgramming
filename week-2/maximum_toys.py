# Complete the maximumToys function below.
def maximumToys(prices, k):
    prices.sort()
    total = 0
    ctr = 0
    while total + prices[ctr] <= k and ctr<len(prices):
        total += prices[ctr]
        ctr +=1
    return ctr
    

